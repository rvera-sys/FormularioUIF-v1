// FETCH - Estrategia de cach脙漏
// 芒聲聬芒聲聬芒聲聬芒聲聬芒聲聬芒聲聬芒聲聬芒聲聬芒聲聬芒聲聬芒聲聬芒聲聬芒聲聬芒聲聬芒聲聬芒聲聬芒聲聬芒聲聬芒聲聬芒聲聬芒聲聬芒聲聬芒聲聬芒聲聬芒聲聬芒聲聬芒聲聬芒聲聬芒聲聬芒聲聬芒聲聬芒聲聬芒聲聬芒聲聬芒聲聬芒聲聬芒聲聬芒聲聬芒聲聬芒聲聬芒聲聬芒聲聬芒聲聬芒聲聬芒聲聬芒聲聬芒聲聬芒聲聬芒聲聬芒聲聬芒聲聬芒聲聬芒聲聬芒聲聬芒聲聬芒聲聬芒聲聬芒聲聬芒聲聬芒聲聬芒聲聬芒聲聬芒聲聬
self.addEventListener('fetch', event => {
  const url = new URL(event.request.url);
  
  // APIs externas: Network-first (con fallback a cach脙漏)
  if (url.origin !== location.origin) {
    event.respondWith(
      fetch(event.request)
        .then(response => {
          // Cachear si es exitoso
          if (response.ok) {
            const clone = response.clone();
            caches.open(CACHE).then(cache => cache.put(event.request, clone));
          }
          return response;
        })
        .catch(() => caches.match(event.request))
    );
    return;
  }
  
  // version.json: SIEMPRE desde red para detectar actualizaciones
  if (url.pathname.endsWith('version.json')) {
    event.respondWith(
      fetch(event.request, { cache: 'no-store' })
        .catch(() => caches.match(event.request))
    );
    return;
  }
  
  // Assets locales: Cache-first (con actualizaci脙鲁n en background)
  event.respondWith(
    caches.match(event.request)
      .then(cached => {
        if (cached) {
          // Actualizar en background
          fetch(event.request).then(response => {
            if (response.ok) {
              caches.open(CACHE).then(cache => cache.put(event.request, response));
            }
          }).catch(() => {});
          
          return cached;
        }
        
        // No est脙隆 en cach脙漏, traer de red
        return fetch(event.request).then(response => {
          if (response.ok) {
            const clone = response.clone();
            caches.open(CACHE).then(cache => cache.put(event.request, clone));
          }
          return response;
        });
      })
  );
});

